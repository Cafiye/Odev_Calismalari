public class Song {
    private String name;
    private int uzunluk ;

    public Song(String name, int uzunluk) {
        this.name = name;
        this.uzunluk = uzunluk;
    }

    public String getName() {
        return name;
    }
    public int getUzunluk() {
        return uzunluk;
    }
}
